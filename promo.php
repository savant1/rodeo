<hr class="dotted" />
<strong>HD-TCX</strong> (High Definition &#8211; Transmission over Coax Cables) is a new technology developed to transmit HD video via coaxial cable, allowing for reliable and non-latency long range HD transmission without additions of relay devices. The technology renders Full HD 1080p &#038; HD 720p megapixel video formats by progressive scanning in an uncompressed and crosstalk-free environment with constant signal transmission and display without problems such as frame drop &#038; lock loss occurrences.
</br></br>
<strong>HD-TCX</strong> also allows for two-way communication such as Camera OSD Access along with the capability of Digital Zoom in live view or playback. The installation of <strong>HD-TCX</strong> Cameras &#038; DVR is far easier as it allows the use of existing RG-59 coax cable to easily upgrade the current analog system by successfully transmitting HD video over distances in excess of 500 meters with no expensive repeaters or signal boosters.
